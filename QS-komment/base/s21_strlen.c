#include "../s21_string.h"

s21_size_t s21_strlen(const char *str) {
  s21_size_t str_length = 0;  // Переменная для хранения длины строки
  
  // Цикл, который продолжается, пока не встретится нулевой символ '\0'
  while (str[str_length] != '\0') {
    str_length++;  // Увеличиваем счетчик длины
  }
  
  return str_length;  // Возвращаем длину строки
}



/*
### Переменная `length`

Переменная `length` в функции `s21_strlen` используется для хранения текущей длины строки, то есть количества символов в строке, не включая нулевой символ (`'\0'`), который обозначает конец строки.

### Подробное объяснение

1. **Инициализация**:
   ```c
   s21_size_t length = 0;
   ```
   - Переменная `length` инициализируется нулем. Это означает, что на начальном этапе длина строки равна нулю, так как мы еще не начали считать символы.

2. **Цикл `while`**:
   ```c
   while (str[length] != '\0') {
     length++;
   }
   ```
   - Цикл `while` выполняется до тех пор, пока текущий символ строки `str[length]` не равен нулевому символу (`'\0'`).
   - В каждом цикле `length` увеличивается на 1, что означает, что мы переходим к следующему символу строки.
   - Этот процесс продолжается, пока не будет достигнут конец строки, обозначенный нулевым символом (`'\0'`).

3. **Возврат длины строки**:
   ```c
   return length;
   ```
   - После завершения цикла `while` переменная `length` будет содержать количество символов в строке до нулевого символа.
   - Функция возвращает это значение, которое и является длиной строки.

### Пример

Рассмотрим пример строки `"Hello"`:

1. **Инициализация**:
   - `length = 0`.

2. **Первый проход цикла**:
   - `str[0]` содержит `'H'`, который не равен `'\0'`.
   - `length` увеличивается до `1`.

3. **Второй проход цикла**:
   - `str[1]` содержит `'e'`, который не равен `'\0'`.
   - `length` увеличивается до `2`.

4. **Третий проход цикла**:
   - `str[2]` содержит `'l'`, который не равен `'\0'`.
   - `length` увеличивается до `3`.

5. **Четвертый проход цикла**:
   - `str[3]` содержит `'l'`, который не равен `'\0'`.
   - `length` увеличивается до `4`.

6. **Пятый проход цикла**:
   - `str[4]` содержит `'o'`, который не равен `'\0'`.
   - `length` увеличивается до `5`.

7. **Шестой проход цикла**:
   - `str[5]` содержит `'\0'`, который обозначает конец строки.
   - Цикл завершается.

### Заключение

Переменная `length` хранит количество символов в строке до нулевого символа (`'\0'`). В процессе выполнения функции `s21_strlen` она последовательно увеличивается, проходя по каждому символу строки, пока не достигнет конца строки. После завершения цикла `while` переменная `length` содержит длину строки, которая затем возвращается функцией.
*/